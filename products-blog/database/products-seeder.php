/* * * mysql hostname ** */
$hostname = "localhost";
/* * * mysql username ** */
$username = "root";
/* * * mysql password ** */
$password = "";
/* * * mysql database name ** */
$database = "shoe_shop";

try {
    $connection = new PDO("mysql:host=$hostname;dbname=$database;charset=utf8", $username, $password);
    $connection->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);

    foreach ($products as $product) {
        $sqlQueryString = "INSERT INTO shoe_products (title, description, price, stock, img, status, barcode, brand, category)"
            . " VALUES (" . ":title" . ", " . ":description" . ", " . ":price" . ", " . ":stock" . ", " . ":img" . ", "
            . ":status" . ", " . ":barcode" . ", " . ":brand" . ", " . ":category" . ")";
        $statement = $connection->prepare($sqlQueryString);
        $podaci = [
            'title' => $product["title"],
            'description' => $product["description"],
            'price' =>  $product["price"],
            'stock' => rand(1, 10),
            'img' => $product["img"],
            'status' => $product["available"],
            'barcode' => $product["id"],
            'brand' => $product["brand"],
            'category' => $product["category"]
        ];
        $status = $statement->execute($podaci);
    }
} catch (PDOException $exception) {
    echo "Something went wrong: " . $exception->getMessage() . "<br>";
}
